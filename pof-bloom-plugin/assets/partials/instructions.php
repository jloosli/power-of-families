<h2>Welcome to the Bloom Game!</h2>
<p>Please read over these game instructions, then click &ldquo;Ready to Play&rdquo; at the bottom.</p>
<p>Don&rsquo;t worry &mdash; you don&rsquo;t have to remember all of the instructions below. We&rsquo;ll remind you of
    what you need to do each step of the way. But a quick read-through of these instructions now will help you
    get the &ldquo;big picture.&rdquo;</p>
<h3>Purpose of the Game</h3>
<p>It feels so good to accomplish things, but we often get overwhelmed when we see the distance between us and
    our big goals. The solution? Play the Bloom Game!</p>
<p>We&rsquo;ll empower you to experience more:</p>
<ul>
    <li><strong>Accomplishment:</strong> Set and complete &ldquo;do-able,&rdquo; valuable goals each week with 
        help from all our goal suggestions.</li>
    <li><strong>Balance:</strong> Select bite-sized goals for You, Your Family, and Beyond.</li>
    <li><strong>Serendipity:</strong> Embrace unexpected opportunities to learn, teach and enjoy life.</li>
</ul>
<h3>Set Up</h3>
<h4>Self Assessment</h4>
<p>When you finish reading these instructions, we&rsquo;ll take you to the Bloom Self-Assessment (it&rsquo;ll take you about 
    10 minutes to fill this out). The Self-Assessment will help you see how you&rsquo;re currently doing in different 
    areas of your life and establish a &ldquo;baseline&rdquo; to compare yourself against as you move forward with the 
    game. You&rsquo;ll be able to refer to your assessment scores to help you determine which goals you want to set.</p>
<p>We&rsquo;ll prompt you to take the same Self-Assessment again every 3 months and it&rsquo;ll be great to see your 
    scores increasing. You&rsquo;ll be able to track your progress and literally see yourself growing and becoming 
    &#8211; and blooming!</p>
<h4>Level of Play</h4>
<p>After you&rsquo;ve taken the Self-Assessment, you&rsquo;ll be taken to your &ldquo;Preferences&rdquo; screen where you can 
    select whether you&rsquo;d like to start with the beginner or advanced level. What&rsquo;s included in each level 
    is explained in the &ldquo;Scoring Points&rdquo; section below.</p>
<h4>Number of Players</h4>
<p>Play on your own or invite friends or relatives to be on your team. On your &ldquo;Preferences&rdquo; screen,
    you&rsquo;ll be able to enter the email addresses of those you&rsquo;d like to invite. Research shows that sharing
    our goals increases the likelihood of achieving them. Being accountable to your friends is a great incentive, and
    ALL of you will benefit. You can start off playing on your own and add &ldquo;team members&rdquo; later if you want.</p>
<h3>Beginning your First Round of Play</h3>
<p>When you&rsquo;ve finished the set-up steps above, you&rsquo;re ready to set your goals and start playing!
    You&rsquo;ll be directed to a screen with lots of specific ideas for meaningful, bite-sized goals in the
    three categories, &ldquo;For You,&rdquo; &ldquo;For Your Family,&rdquo; and &ldquo;Beyond.&rdquo; You&rsquo;ll
    simply click on the goals you want to do, adjust them to fit your needs (or make up your own goals if you want),
    then a few minutes later &#8211; voila! Your own personal chart with your goals for this week is generated for you.
    Now you&rsquo;re ready to play.</p>
<h3>Scoring Points</h3>
<p>There are two ways to get points each week:</p>
<ul>
    <li><strong>Completing Goals:</strong> You get a point for each goal you set and accomplish (3 in the beginner
        level, 5 in the advanced level)</li>
    <li><strong>Serendipity Moments:</strong> You get a point for embracing and recording the spontaneous moments of
        giving, growing and enjoying that you notice in your life (2 in the beginner level, 3 in the advanced level).
        You can check out <a href="javascript:window.location.search='page=serendipity-examples'">examples of &ldquo;Serendipity
            Moments&rdquo; here</a>.</li>
</ul>
<p>A &ldquo;perfect&rdquo; score for the beginner level would be 5 and a perfect score for the advanced level would be 8.</p>
<p>Your score is something only you and our website administrator will see unless you choose to set up a team and
    enable everyone on your team to see each other&rsquo;s goals and scores.</p>
<h3>Method of Play</h3>
<p>Throughout the week, you&rsquo;ll track your accomplishments and Serendipity Moments on the personal tracking sheet
    we&rsquo;ll help you create (you can do this online or you can print out your goal sheet). When you have a
    &ldquo;serendipitous&rdquo; moment arise (a chance to answer a child&rsquo;s question with an important discussion,
    a chance to help a friend in need, a beautiful day when you announce a spontaneous family picnic or bike ride), you
    write that down (or type it in) in the space provided on your tracking sheet (this is a point-earning
    &ldquo;accomplishment&rdquo; too!).</p>
<h3>Ongoing Play</h3>
<p>Each Sunday, you will receive an email with a link to the goal chart you made the previous Sunday. You&rsquo;ll
    check off any goals you&rsquo;ve completed and didn&rsquo;t check off during the week and record Serendipity
    Moments you haven&rsquo;t previously recorded. Your score for the week will be calculated for you and you&rsquo;ll
    be invited to share any favorite accomplishments (planned or unplanned) for that week with the whole community of
    Bloom players. You can read about other&rsquo;s accomplishments and gain new ideas and inspiration.</p>
<p>Then you&rsquo;ll set your goals for the upcoming week.</p>
<p>You can look back at goals you&rsquo;ve set in the past and past week scores as well as your Self-Assessment scores
    at any time.</p>
<p>Every three months, we&rsquo;ll prompt you to re-take your Self-Assessment.</p>
<h3>Rewards</h3>
<p>The most important reward you receive will be the sense of accomplishment and progress this program will provide.</p>
<p>But you may want to come up with your own special reward. You could decide that if you get a perfect score for some
    set number of weeks, you&rsquo;ll buy yourself something you&rsquo;ve been wanting. It&rsquo;s always nice to have
    a fun little &ldquo;carrot&rdquo; you dangle out there for yourself!</p>
<p>If you&rsquo;re doing this with friends or family members, you could come up with a fun joint prize for everyone who
    has earned a certain number of points after a given period of time (like everyone with over 60 points after an
    8-week period will go to lunch together &#8211; or go on a fun girls&rsquo; weekend together!).</p>
<p>You can have fun with rewards or incentives. Or you can just participate because it&rsquo;ll make you feel great and
    that&rsquo;s reward enough.</p>
<h3>Playing with Others</h3>
<p>To play with friends or family members, simply copy, paste and edit the following into an email or Facebook post.</p>
<p><em>&ldquo;I&rsquo;m using the Power of Moms Bloom Game to help me accomplish simple bite-sized goals each week.
        Want to &ldquo;play&rdquo; with me? We can compare scores each week and help each other stay on track towards
        the things that are important to us. And let&rsquo;s treat ourselves to a fun girls&rsquo; night out (or
        whatever else we think would be fun) if we can each get 60 points during an 8-week period.  Read about the
        game and sign up <a href="http://powerofmoms.com/empowering-opportunities/the-bloom-game/">HERE</a> then let
        me know you&rsquo;re going to be part of my group so I can follow up with you!&rdquo;</em></p>
<p>Then create an email group (just a list of emails you keep handy or a yahoo or gmail group) or Facebook group with
    those who&rsquo;ve agreed to play the game with you.</p>
<p>Each week, when you get your Bloom coaching email from us on Sunday, send a Facebook post or email to your group,
    asking everyone to report their score. You could say something like this:</p>
<p><em>&ldquo;How did Bloom go for you last week? Report your score by replying to this post/email!&rdquo;</em></p>
